    private void BTNConfirmarFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNConfirmarFinalizarActionPerformed
        if (totalPaid >= totalPrice){
            double totalPrice = this.totalPrice;
            double totalPaid = this.totalPaid;
            double totalInCash = this.totalInCash;
            boolean isDiscount = CHECKDesconto.isSelected();
            String client_name = this.client_name;
            String client_cpf = this.client_cpf;
            String date = this.date;
            double cash = Double.parseDouble(TXTDinheiro.getText());
            double debit = Double.parseDouble(TXTDebito.getText());
            double pix = Double.parseDouble(TXTPIX.getText());
            double link = Double.parseDouble(TXTLink.getText());
            double credit = Double.parseDouble(TXTCredito.getText());
            int installments = CBParcelas.getSelectedIndex() + 1;
            String client_address = TXTEndereco.getText() + ", " + TXTNumero.getText() + ", " + TXTCEP.getText();
            String client_address_ref = TXTReferencia.getText();
            DefaultTableModel cart = this.cart;
            
            ViewportConfirmSale viewCS = new ViewportConfirmSale(totalPrice, totalPaid, totalInCash, isDiscount, client_name, client_cpf, date, cash, debit, pix, link, credit, installments, client_address, client_address_ref, cart);

            viewCS.setVisible(true);
        }
        else{
            totalPaid = 0;
            totalInCash = 0;
            JOptionPane.showMessageDialog(null, "Não pagou todo o valor da compra!");
            UpdateSelectedIndex(0);
        }
    }//GEN-LAST:event_BTNConfirmarFinalizarActionPerformed


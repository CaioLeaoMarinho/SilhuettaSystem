    private void BUTTONPreencherClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BUTTONPreencherClienteActionPerformed
        String clientCPF = TXTCPFCliente.getText();
        ClientsModel client = new ClientsModel();
        ClientsDAO dao = new ClientsDAO();

        client = dao.consultClientByCPF(clientCPF);

        if (client.getName() != null) {
            TXTNomeCliente.setText(client.getName());
        } else {
            JOptionPane.showMessageDialog(null, "FALHA ao ENCONTRAR Cliente de CPF: " + clientCPF);
        }
    }//GEN-LAST:event_BUTTONPreencherClienteActionPerformed

    private void BUTTONPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BUTTONPesquisarActionPerformed
        int productID = Integer.parseInt(TXTID.getText());
        ProductsModel product = new ProductsModel();
        StockDAO dao = new StockDAO();

        product = dao.consultProductByID(productID);

        if (product.getProduct() != null) {
            TXTProduto.setText(product.getProduct());
            TXTCor.setText(product.getColor());
            TXTPreco.setText(String.valueOf(product.getPrice()));
        } else {
            JOptionPane.showMessageDialog(null, "FALHA ao ENCONTRAR Produto de ID: " + productID);
        }
    }//GEN-LAST:event_BUTTONPesquisarActionPerformed


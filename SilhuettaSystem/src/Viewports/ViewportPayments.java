    private ArrayList<String> CalculateInstallments() {
        ArrayList<String> plots = new ArrayList<>();

        for (int i = 1; i <= 12; i++) {
            if (Double.parseDouble(TXTCredito.getText().replace(",", ".")) / i >= minimumInstallmentsValue) {
                plots.add(i + "x");
            } else {
                if (plots.isEmpty()) {
                    plots.add("1x");
                }
                break;
            }
        }

        return plots;
    }

    private void UpdateCBParcelas() {
        ArrayList<String> installmentsList = new ArrayList<>();

        DefaultComboBoxModel model = (DefaultComboBoxModel) CBParcelas.getModel();
        model.removeAllElements();
        
        if (!TXTCredito.getText().isBlank()) {
            if (Double.parseDouble(TXTCredito.getText()) > 0) {
                installmentsList = CalculateInstallments();
            }
            else {
                installmentsList.add("1x");
            }
        }
        else {
            installmentsList.add("1x");
        }

        for (String plot : installmentsList) {
            model.addElement(plot);
        }
    }
    
